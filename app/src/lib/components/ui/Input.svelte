<script lang="ts">
  import { cn } from '$lib/utils';
  import type { HTMLInputAttributes } from 'svelte/elements';

  interface Props extends HTMLInputAttributes {
    value?: string;
    class?: string;
  }

  // Make `value` bindable so parent components can use `bind:value`
  let { value = $bindable(''), class: className, ...restProps }: Props = $props();
</script>

<input
  value={value}
  on:input={(e) => (value = (e.currentTarget as HTMLInputElement).value)}
  class={cn(
    'flex h-9 w-full rounded-lg border border-input bg-transparent px-3 py-1 text-sm shadow-sm transition-colors',
    'file:border-0 file:bg-transparent file:text-sm file:font-medium file:text-foreground',
    'placeholder:text-muted-foreground',
    'focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring',
    'disabled:cursor-not-allowed disabled:opacity-50',
    className
  )}
  {...restProps}
/>
